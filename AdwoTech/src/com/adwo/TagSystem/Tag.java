package com.adwo.TagSystem;

/**
 * @author dev
 *
 */
public class Tag {

	private String TagName;
	private int TagId;
	/**
	 * @return the tagName
	 */
	public String getTagName() {
		return TagName;
	}
	/**
	 * @param tagName the tagName to set
	 */
	public void setTagName(String tagName) {
		TagName = tagName;
	}
	/**
	 * @return the tagId
	 */
	public int getTagId() {
		return TagId;
	}
	/**
	 * @param tagId the tagId to set
	 */
	public void setTagId(int tagId) {
		TagId = tagId;
	}
	
}
